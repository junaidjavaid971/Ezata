package app.com.ezata.model;

public class HistoryDetails {
    String date,time,orderNumber, customerName, type,amount,status;

    public HistoryDetails(String date, String time, String orderNumber, String customerName, String type, String amount, String status) {
        this.date = date;
        this.time = time;
        this.orderNumber = orderNumber;
        this.customerName = customerName;
        this.type = type;
        this.amount = amount;
        this.status = status;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getOrderNumber() {
        return orderNumber;
    }

    public void setOrderNumber(String orderNumber) {
        this.orderNumber = orderNumber;
    }

    public String getCustomerName() {
        return customerName;
    }

    public void setCustomerName(String customerName) {
        this.customerName = customerName;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
